def _bash_installer(config: RepositoryConfig, pack: Pack) -> str:
    skills = "\n".join(f'  "{skill}"' for skill in pack.skills)
    default_pin = pack.source_sha or "UNPUBLISHED"
    unpublished = 0 if pack.source_sha else 1
    return f"""#!/usr/bin/env bash
# Generated by tools/generate-installers. Do not edit by hand.
set -euo pipefail

repository="${{GENAPTIC_SKILLSETS_REPOSITORY:-{config.slug}}}"
agent="opencode"
scope="user"
pin="${{GENAPTIC_SKILLSETS_REF:-{default_pin}}}"
force=0
dry_run=0
unpublished={unpublished}

usage() {{
  cat <<'EOF'
Install the {pack.display_name} skillpack with GitHub CLI.

Usage:
  {pack.id}.sh [options]

Options:
  --repository ORG/PROJECT  Source repository.
  --agent AGENT             Target agent (default: opencode).
  --scope SCOPE             user or project (default: user).
  --pin REF                 Pack tag or full commit SHA.
  --force                   Overwrite installed skills.
  --dry-run                 Print commands without executing them.
  -h, --help                Show this help.

The installer invokes only the public-preview `gh skill install` interface. It checks both
`gh skill --help` and `gh skill install --help` before making changes. Dry runs do not require
or probe `gh`. Every probe and install invocation sets `GH_TELEMETRY=false`,
`GH_PROMPT_DISABLED=1`, and `GH_NO_UPDATE_NOTIFIER=1` for that invocation only. The installer
does not execute bundled skill scripts.
EOF
}}

while [[ $# -gt 0 ]]; do
  case "$1" in
    --repository) repository="${{2:?missing value for --repository}}"; shift 2 ;;
    --agent) agent="${{2:?missing value for --agent}}"; shift 2 ;;
    --scope) scope="${{2:?missing value for --scope}}"; shift 2 ;;
    --pin) pin="${{2:?missing value for --pin}}"; shift 2 ;;
    --force) force=1; shift ;;
    --dry-run) dry_run=1; shift ;;
    -h|--help) usage; exit 0 ;;
    *) echo "Unknown argument: $1" >&2; usage >&2; exit 2 ;;
  esac
done

[[ "$repository" =~ ^[A-Za-z0-9_.-]+/[A-Za-z0-9_.-]+$ ]] || {{
  echo "--repository must use ORG/PROJECT format." >&2
  exit 2
}}
[[ "$scope" == "user" || "$scope" == "project" ]] || {{
  echo "--scope must be user or project." >&2
  exit 2
}}
[[ -n "$agent" && "$agent" != -* ]] || {{
  echo "--agent must be a non-empty agent identifier." >&2
  exit 2
}}
[[ -n "$pin" && "$pin" != -* ]] || {{
  echo "--pin must be a tag or full commit SHA." >&2
  exit 2
}}
[[ "$dry_run" -eq 1 || "$pin" != "UNPUBLISHED" ]] || {{
  echo "This pack is unpublished; pass --pin with a reviewed full commit SHA." >&2
  exit 2
}}
if [[ "$dry_run" -eq 0 && "$unpublished" -eq 1 && ! "$pin" =~ ^[0-9a-fA-F]{{40}}$ ]]; then
  echo "Unpublished packs require --pin with a full 40-character commit SHA." >&2
  exit 2
fi

if [[ "$dry_run" -eq 0 ]]; then
  if ! command -v gh >/dev/null 2>&1; then
    echo "GitHub CLI (gh) is not installed; the optional direct installer is unavailable." >&2
    exit 127
  fi
  if ! GH_TELEMETRY=false GH_PROMPT_DISABLED=1 GH_NO_UPDATE_NOTIFIER=1 gh skill --help >/dev/null 2>&1; then
    echo "Installed GitHub CLI does not support the public-preview 'gh skill' command." >&2
    exit 1
  fi
  if ! GH_TELEMETRY=false GH_PROMPT_DISABLED=1 GH_NO_UPDATE_NOTIFIER=1 gh skill install --help >/dev/null 2>&1; then
    echo "Installed GitHub CLI does not support the public-preview 'gh skill install' command." >&2
    exit 1
  fi
fi

skills=(
{skills}
)

for skill in "${{skills[@]}}"; do
  path="{pack.relative_path}/skills/$skill"
  command=(gh skill install "$repository" "$path" --agent "$agent" --scope "$scope" --pin "$pin")
  if [[ "$force" -eq 1 ]]; then
    command+=(--force)
  fi
  if [[ "$dry_run" -eq 1 ]]; then
    printf '%q ' "${{command[@]}}"
    printf '\\n'
  else
    printf 'Installing %s for %s at %s scope...\\n' "$skill" "$agent" "$scope"
    GH_TELEMETRY=false GH_PROMPT_DISABLED=1 GH_NO_UPDATE_NOTIFIER=1 "${{command[@]}}"
  fi
done
"""


def _powershell_installer(config: RepositoryConfig, pack: Pack) -> str:
    skills = ",\n    ".join(f'"{skill}"' for skill in pack.skills)
    default_pin = pack.source_sha or "UNPUBLISHED"
    unpublished = "$true" if not pack.source_sha else "$false"
    return f"""# Generated by tools/generate-installers. Do not edit by hand.
[CmdletBinding(SupportsShouldProcess = $true)]
param(
    [ValidatePattern('^[A-Za-z0-9_.-]+/[A-Za-z0-9_.-]+$')]
    [string]$Repository = $(if ($env:GENAPTIC_SKILLSETS_REPOSITORY) {{
        $env:GENAPTIC_SKILLSETS_REPOSITORY
    }} else {{
        "{config.slug}"
    }}),

    [ValidateNotNullOrEmpty()]
    [string]$Agent = "opencode",

    [ValidateSet("user", "project")]
    [string]$Scope = "user",

    [ValidateNotNullOrEmpty()]
    [string]$Pin = $(if ($env:GENAPTIC_SKILLSETS_REF) {{
        $env:GENAPTIC_SKILLSETS_REF
    }} else {{
        "{default_pin}"
    }}),

    [switch]$Force,
    [switch]$DryRun
)

$ErrorActionPreference = "Stop"
$Unpublished = {unpublished}

if (-not $DryRun -and $Pin -eq "UNPUBLISHED") {{
    throw "This pack is unpublished; pass -Pin with a reviewed full commit SHA."
}}
if (-not $DryRun -and $Unpublished -and $Pin -notmatch '^[0-9a-fA-F]{{40}}$') {{
    throw "Unpublished packs require -Pin with a full 40-character commit SHA."
}}

$GhCommand = $null
if (-not $DryRun) {{
    $GhCommand = @(Get-Command gh -CommandType Application -ErrorAction SilentlyContinue)[0]
    if (-not $GhCommand) {{
        Write-Error -Message (
            "GitHub CLI (gh) is not installed; the optional direct installer is unavailable."
        ) -ErrorAction Continue
        exit 127
    }}
}}

$skills = @(
    {skills}
)

$GhEnvironment = [ordered]@{{
    "GH_TELEMETRY" = "false"
    "GH_PROMPT_DISABLED" = "1"
    "GH_NO_UPDATE_NOTIFIER" = "1"
}}
$GhEnvironmentSnapshot = @{{}}

if (-not $DryRun) {{
    foreach ($name in $GhEnvironment.Keys) {{
        $entry = Get-Item -LiteralPath "Env:$name" -ErrorAction SilentlyContinue
        $GhEnvironmentSnapshot[$name] = [pscustomobject]@{{
            Present = $null -ne $entry
            Value = if ($null -ne $entry) {{ $entry.Value }} else {{ $null }}
        }}
    }}
}}

try {{
    if (-not $DryRun) {{
        foreach ($name in $GhEnvironment.Keys) {{
            Set-Item -LiteralPath "Env:$name" -Value $GhEnvironment[$name]
        }}

        & $GhCommand.Source "skill" "--help" *> $null
        if ($LASTEXITCODE -ne 0) {{
            throw "Installed GitHub CLI does not support the public-preview 'gh skill' command."
        }}
        & $GhCommand.Source "skill" "install" "--help" *> $null
        if ($LASTEXITCODE -ne 0) {{
            throw (
                "Installed GitHub CLI does not support the public-preview " +
                "'gh skill install' command."
            )
        }}
    }}

foreach ($skill in $skills) {{
    $path = "{pack.relative_path}/skills/$skill"
    $arguments = @(
        "skill", "install", $Repository, $path,
        "--agent", $Agent,
        "--scope", $Scope,
        "--pin", $Pin
    )
    if ($Force) {{
        $arguments += "--force"
    }}

    $display = "gh " + ($arguments | ForEach-Object {{
        if ($_ -match '[\\s"]') {{ '"' + ($_ -replace '"', '\\"') + '"' }} else {{ $_ }}
    }} ) -join " "

    if ($DryRun) {{
        Write-Output $display
        continue
    }}

    if ($PSCmdlet.ShouldProcess("$skill for $Agent at $Scope scope", $display)) {{
        & $GhCommand.Source @arguments
        if ($LASTEXITCODE -ne 0) {{
            throw "gh skill install failed for $skill with exit code $LASTEXITCODE."
        }}
    }}
}}
}} finally {{
    if (-not $DryRun) {{
        foreach ($name in $GhEnvironment.Keys) {{
            $snapshot = $GhEnvironmentSnapshot[$name]
            if ($snapshot.Present) {{
                Set-Item -LiteralPath "Env:$name" -Value $snapshot.Value
            }} else {{
                Remove-Item -LiteralPath "Env:$name" -ErrorAction SilentlyContinue
            }}
        }}
    }}
}}
"""



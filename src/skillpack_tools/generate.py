def _is_runtime_file(path: Path, skill_dir: Path) -> bool:
    relative = path.relative_to(skill_dir)
    return runtime_resource_is_allowed(relative)


def _runtime_files(root: Path, skill_dir: Path) -> list[Path]:
    paths: list[Path] = []
    for subdir in ("references", "assets", "scripts"):
        folder = skill_dir / subdir
        snapshot = walk_tree(folder, root, allow_missing=True)
        paths.extend(
            path for path, _metadata in snapshot.files if _is_runtime_file(path, skill_dir)
        )
    return paths


def _tracked_source_modes(root: Path) -> dict[str, int]:
    """Return Git-index modes once so generation remains fast and cross-platform."""

    try:
        result = subprocess.run(
            ["git", "-C", str(root), "ls-files", "--stage", "-z"],
            check=False,
            capture_output=True,
            text=True,
        )
    except OSError:
        return {}
    if result.returncode != 0:
        return {}
    modes: dict[str, int] = {}
    for entry in result.stdout.split("\0"):
        if not entry or "\t" not in entry:
            continue
        metadata, relative = entry.split("\t", 1)
        index_mode = metadata.split(maxsplit=1)[0]
        modes[relative] = 0o755 if index_mode == "100755" else 0o644
    return modes


def _portable_source_mode(
    root: Path, path: Path, tracked_modes: dict[str, int] | None = None
) -> int:
    """Read the tracked executable bit portably, falling back to filesystem mode."""

    try:
        relative = path.relative_to(root).as_posix()
    except ValueError:
        relative = ""
    modes = tracked_modes if tracked_modes is not None else _tracked_source_modes(root)
    if relative in modes:
        return modes[relative]
    metadata = inspect_path(path, root, leaf_kind="file")
    assert metadata is not None
    return 0o755 if metadata.st_mode & 0o111 else 0o644


def build_generated_files(root: Path) -> GeneratedFiles:
    root = Path(os.path.abspath(os.fspath(root)))
    config = load_repository(root)
    packs = discover_packs(root)
    if not packs:
        raise SkillpackError("No skillpack.yaml files were found under packs/*/*/.")
    tracked_modes = _tracked_source_modes(root)

    files = GeneratedFiles()
    for pack in packs:
        if "claude-code" in pack.targets:
            files.add(
                f"{pack.relative_path}/.claude-plugin/plugin.json",
                json_text(_claude_plugin(config, pack)),
            )
        if "codex" in pack.targets:
            files.add(
                f"{pack.relative_path}/.codex-plugin/plugin.json",
                json_text(_codex_plugin(config, pack)),
            )

    files.add(".claude-plugin/marketplace.json", json_text(_claude_marketplace(config, packs)))
    files.add(".agents/plugins/marketplace.json", json_text(_codex_marketplace(config, packs)))
    files.add("catalog.json", json_text(_catalog(config, packs)))

    root_readme = read_regular_text(root / "README.md", root)
    files.add(
        "README.md",
        replace_marked_section(
            root_readme, GENERATED_BEGIN, GENERATED_END, _root_catalog_markdown(config, packs)
        ),
    )

    for pack in packs:
        pack_readme_path = pack.path / "README.md"
        pack_readme_metadata = inspect_path(
            pack_readme_path,
            root,
            leaf_kind="file",
            allow_missing=True,
        )
        if pack_readme_metadata is not None:
            pack_readme = read_regular_text(pack_readme_path, root)
            files.add(
                f"{pack.relative_path}/README.md",
                replace_marked_section(
                    pack_readme,
                    PACK_INSTALL_BEGIN,
                    PACK_INSTALL_END,
                    _pack_install_markdown(config, pack),
                ),
            )

    opencode_packs = [pack for pack in packs if "opencode" in pack.targets]
    files.add("dist/install/README.md", _installer_readme(config, opencode_packs))
    files.add("dist/opencode/README.md", _opencode_readme(opencode_packs))

    for pack in opencode_packs:
        files.add(f"dist/install/{pack.id}.sh", _bash_installer(config, pack), mode=0o755)
        files.add(f"dist/install/{pack.id}.ps1", _powershell_installer(config, pack))

        catalog_base = Path("dist/opencode") / pack.language / pack.subject
        catalog_skills: list[dict] = []
        for skill in pack.skills:
            skill_dir = pack.path / "skills" / skill
            source_skill = skill_dir / "SKILL.md"
            runtime = _runtime_files(root, skill_dir)
            listed_files = [f"{skill}.md"]
            target_prefix = catalog_base / skill
            files.add(
                (target_prefix / f"{skill}.md").as_posix(),
                read_regular_bytes(source_skill, root),
            )
            for resource in runtime:
                relative = resource.relative_to(skill_dir)
                listed_files.append(relative.as_posix())
                mode = _portable_source_mode(root, resource, tracked_modes)
                files.add(
                    (target_prefix / relative).as_posix(),
                    read_regular_bytes(resource, root),
                    mode=mode,
                )
            catalog_skills.append({"name": skill, "version": pack.version, "files": listed_files})
        files.add(
            (catalog_base / "index.json").as_posix(),
            json_text({"skills": catalog_skills}),
        )

    manifest_entries = [
        {
            "path": path,
            "sha256": sha256_bytes(_as_bytes(content)),
            "size": len(_as_bytes(content)),
            "mode": f"{files.modes[path]:04o}",
        }
        for path, content in sorted(files.items())
        if path != "dist/generated-files.json"
    ]
    files.add(
        "dist/generated-files.json",
        json_text({"schemaVersion": 1, "files": manifest_entries}),
    )
    return files


def apply_generated_files(root: Path, *, check: bool = False) -> list[str]:
    root = Path(os.path.abspath(os.fspath(root)))
    canonical_before = walk_tree(root / "packs", root)
    root_source_preimages = {
        path: inspect_path(path, root, leaf_kind="file")
        for path in (root / "repository.yaml", root / "README.md")
    }
    expected = build_generated_files(root)
    changed: list[str] = []

    canonical_after = walk_tree(root / "packs", root)
    before_nodes = {
        repository_relative(path, root): metadata
        for path, metadata in (*canonical_before.directories, *canonical_before.files)
    }
    after_nodes = {
        repository_relative(path, root): metadata
        for path, metadata in (*canonical_after.directories, *canonical_after.files)
    }
    if set(before_nodes) != set(after_nodes) or any(
        not same_identity(metadata, after_nodes[path]) for path, metadata in before_nodes.items()
    ):
        raise SkillpackError("Canonical pack content changed during generation preflight.")
    for path, preimage in root_source_preimages.items():
        assert preimage is not None
        verify_preimage(path, root, preimage)

    packs = discover_packs(root)
    generated_roots = [
        root / ".claude-plugin",
        root / ".agents" / "plugins",
        root / "dist" / "install",
        root / "dist" / "opencode",
        *(pack.path / ".claude-plugin" for pack in packs),
        *(pack.path / ".codex-plugin" for pack in packs),
    ]

    expected_paths: dict[str, tuple[Path, os.stat_result | None]] = {}
    for relative in expected:
        raw_relative = Path(relative)
        if raw_relative.is_absolute() or ".." in raw_relative.parts:
            raise SkillpackError(f"Generated destination is not repository-relative: {relative!r}.")
        path = root / raw_relative
        normalized = repository_relative(path, root).as_posix()
        if normalized != raw_relative.as_posix():
            raise SkillpackError(f"Generated destination is not normalized: {relative!r}.")
        preimage = inspect_path(path, root, leaf_kind="file", allow_missing=True)
        expected_paths[relative] = (path, preimage)

    cleanup_files: dict[Path, os.stat_result] = {}
    cleanup_directories: dict[Path, os.stat_result] = {}
    for generated_root in generated_roots:
        snapshot = walk_tree(generated_root, root, allow_missing=True)
        cleanup_files.update(snapshot.files)
        cleanup_directories.update(snapshot.directories)

    for relative, content in expected.items():
        path, preimage = expected_paths[relative]
        wanted = _as_bytes(content)
        metadata = verify_preimage(path, root, preimage)
        actual = read_regular_bytes(path, root) if metadata is not None else None
        actual_mode = metadata.st_mode & 0o777 if metadata is not None else None
        expected_mode = expected.modes[relative]
        mode_mismatch = os.name != "nt" and actual_mode != expected_mode
        if actual != wanted or mode_mismatch:
            changed.append(relative)
            if not check:
                safe_atomic_write_bytes(
                    path,
                    wanted,
                    root,
                    mode=expected_mode,
                    expected=preimage,
                )

    expected_relative_paths = set(expected_paths)
    stale_files: list[tuple[Path, os.stat_result]] = []
    for path, metadata in cleanup_files.items():
        relative = repository_relative(path, root).as_posix()
        if relative not in expected_relative_paths:
            changed.append(relative)
            stale_files.append((path, metadata))

    if not check:
        for path, metadata in sorted(stale_files, key=lambda item: item[0].as_posix()):
            unlink_regular(path, root, expected=metadata)
        for directory, metadata in sorted(
            cleanup_directories.items(),
            key=lambda item: (len(item[0].parts), item[0].as_posix()),
            reverse=True,
        ):
            remove_empty_directory(directory, root, expected=metadata)

    if check and changed:
        detail = "\n".join(f"  - {path}" for path in sorted(set(changed)))
        raise SkillpackError(
            "Generated files are missing or stale. Run `python3 tools/generate-all`:\n" + detail
        )
    return sorted(set(changed))
